package DuringUttara;
public class EscapeSequences
{
	public static void main(String[] args)
	{
	System.out.println("hi \"manoj\" saini");
	System.out.println("hi \'manoj\' saini");
	System.out.println("hi \\manoj\\ saini");
	System.out.println("hi\tmanoj\tsaini");
	System.out.println("hi\nmanoj\nsaini");
	System.out.println("hi \bmanoj\" saini");
	System.out.println("hi \rmanoj\" saini");
	System.out.println("hi \fmanoj\" saini");
	}
}